void print_error(PVRSRV_ERROR error)
{
	const char *errorname =
	        (error < ARRAY_SIZE(errornames)) ?
		errornames[error] : NULL;
	printf("\t\terror:\t\t%08x (%s)\n", error,
		errorname ? errorname : "unknown");
}

