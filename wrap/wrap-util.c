void print_attribs(IMG_UINT32 attribs)
{
	int previous = 0;
	int flag;
	int i;
	const char *memname;

	printf("\t\tattribs:\t%08x (", attribs);

	for (i = 0; i < 32; i++)
	{
		flag = 1 << i;
		if (attribs & flag) {
			if (previous)
				printf(" | ");
			previous = 1;

			memname = (i < ARRAY_SIZE(memnames)) ? memnames[i] : NULL;
			if (memname)
				printf("%s", memname);
			else
				printf("unknown mem attribut 0x%x", flag);
		}
	}

	printf(")\n");
}

